class water:
    ''' A general water class '''

    def __init__(self, wet):
        self.water_content = wet
        self.frequency = 3.0

    def Rain():
         water_content -= 25
         return 25.0

    def Evaporate():
         water_content -= 4
         return -4.0

    def Snow():
         water_content -= 15
         return 15.0